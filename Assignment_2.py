# 3)Comparision operators / Relational operators
x=20
y=4
print(">=",x>=y)
print("<=",x<=y)
print(">",x>y)
print("<",x<y)
print("==",x==y)
print("!=",x!=y)

# 4) Logical operators
x=True
y=False
print("and",x and y)
print("or",x or y)
print("not",not y)

# 5)Bitwise operators
x=10
y=5
print("bitwise &",x&y)
print("bitwise |",x|y)
print("bitwise ^",x^y)
print("bitwise >>",x>>y)
print("bitwise <<",x<<y)


# 6)Identity operators
x=10
y=5
print("is",x is y)
print("is not",x is not y)

#7)Membership operators
