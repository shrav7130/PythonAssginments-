# 1) Arithmetic operators
x=20
y=10
print("addition of two nums: ",x+y)
print("suytraction of two nums: ",x-y)
print("multiplication of two nums: ",x*y)
print("division of two nums: ",x/y)
print("floor division of two nums: ",x//y)
print("exponent : ",x**y)
print("mod: ",x%y)

# 2) Assignment operators
